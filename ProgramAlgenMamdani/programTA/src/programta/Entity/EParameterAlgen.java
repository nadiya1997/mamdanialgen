package programta.Entity;

/**
 *
 * @author Nadiya
 */
public class EParameterAlgen {
    int populasi, itersi;
    double cr, mr;

    public EParameterAlgen() {
    }

    public int getPopulasi() {
        return populasi;
    }

    public void setPopulasi(int populasi) {
        this.populasi = populasi;
    }

    public int getItersi() {
        return itersi;
    }

    public void setItersi(int itersi) {
        this.itersi = itersi;
    }

    public double getCr() {
        return cr;
    }

    public void setCr(double cr) {
        this.cr = cr;
    }

    public double getMr() {
        return mr;
    }

    public void setMr(double mr) {
        this.mr = mr;
    }
}
